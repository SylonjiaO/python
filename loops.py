#while loop function
counter=1
#1-10
while(counter<=10):
    print(counter)
    counter+=1

print("End of Program")
#1-9
while(counter!=10):
    print(counter)
    counter+=1

print("End of Program")

#never ending loop
while(counter!=10):
    print(counter)
    counter+=2

print("End of Program")

#never starting loop
while(counter==10):
    print(counter)
    counter+=2

print("End of Program")
